#include "libft.h"

int	ft_xlen(unsigned int n)
{
	int				i;
	unsigned int	nb;

	i = 0;
	nb = n;
	while (nb > 0)
	{
		nb = nb / 16;
		i++;
	}
	return (n == 0) ? 1 : i;
}
