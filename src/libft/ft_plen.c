#include "libft.h"

int	ft_plen(unsigned long n)
{
	int				i;
	unsigned long	nb;

	nb = n;
	i = 0;
	while (nb > 0)
	{
		nb = nb / 10;
		i++;
	}
	return (n == 0 ? 1 : i);
}
